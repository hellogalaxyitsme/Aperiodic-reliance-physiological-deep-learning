    sham = read_csv(TABLE_DIR / "formal_hypothesis_tests_sham.csv")

    fields = [
        "Domain",
        "Task",
        "Model",
        "Drop flattened BA [95% CI]",
        "Raw p",
        "FDR p",
        "Significant",
    ]

    def clean_task(task: str) -> str:
        return task.replace("_", "-")

    primary_rows = [
        {
            "Domain": row["domain"],
            "Task": clean_task(row["task"]),
            "Model": row["model"],
            "Drop flattened BA [95% CI]": row["effect_95ci"],
            "Raw p": row["p_raw_display"],
            "FDR p": row["p_fdr_display"],
            "Significant": "Yes" if row["significant_fdr"] == "True" else "No",
        }
        for row in primary
    ]

    sham_rows = [
        {
            "Domain": row["domain"],
            "Task": clean_task(row["task"]),
            "Model": row["model"],
            "Sham drop BA [95% CI]": row["effect_95ci"],
            "Raw p": row["p_raw_display"],
            "FDR p": row["p_fdr_display"],
            "Significant": "Yes" if row["significant_fdr"] == "True" else "No",
        }
        for row in sham
    ]

    write_csv(SUPP_DIR / "supplementary_table_8a_formal_primary_tests.csv", primary_rows, fields)
    write_markdown(SUPP_DIR / "supplementary_table_8a_formal_primary_tests.md", primary_rows, fields)
    write_latex_table(
        SUPP_DIR / "supplementary_table_8a_formal_primary_tests.tex",
        "Formal primary flattening-drop tests with Benjamini--Hochberg FDR correction.",
        "tab:formal-primary-tests",
        primary_rows,
        [(field, field) for field in fields],
    )

    sham_fields = [
        "Domain",
        "Task",
        "Model",
        "Sham drop BA [95% CI]",
        "Raw p",
        "FDR p",
        "Significant",
    ]
    write_csv(SUPP_DIR / "supplementary_table_8b_formal_sham_tests.csv", sham_rows, sham_fields)
    write_markdown(SUPP_DIR / "supplementary_table_8b_formal_sham_tests.md", sham_rows, sham_fields)
    write_latex_table(
        SUPP_DIR / "supplementary_table_8b_formal_sham_tests.tex",
        "Formal sham-control tests with Benjamini--Hochberg FDR correction.",
        "tab:formal-sham-tests",
        sham_rows,
        [(field, field) for field in sham_fields],
    )

    combined_path = SUPP_DIR / "supplementary_table_8_formal_hypothesis_tests.tex"
    combined_path.write_text(
        "% Auto-generated by code/scripts/generate_nmi_update_assets.py\n"
        "\\textbf{Supplementary Table 8 | Formal hypothesis tests with BH-FDR correction.} "
        "One-sided bootstrap p-values are reported for primary flattening-drop tests; "
        "two-sided bootstrap p-values are reported for sham-control tests. "
        "Benjamini--Hochberg correction was applied separately to the 31 primary "
        "flattening comparisons and 28 sham comparisons at $q=0.05$.\n\n"
        "\\input{supplementary_table_8a_formal_primary_tests.tex}\n\n"
        "\\input{supplementary_table_8b_formal_sham_tests.tex}\n"
    )


