def hex_to_rgb(color: str) -> tuple[float, float, float]:
    color = color.lstrip("#")
    return tuple(int(color[i : i + 2], 16) / 255 for i in (0, 2, 4))


def pdf_escape(text: object) -> str:
    return str(text).replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")


class SimplePDF:
    """Small vector-PDF helper for bar-chart style paper figures."""

    def __init__(self, width: float, height: float) -> None:
        self.width = width
        self.height = height
        self.ops: list[str] = []

    def stroke_color(self, color: str) -> None:
        r, g, b = hex_to_rgb(color)
        self.ops.append(f"{r:.3f} {g:.3f} {b:.3f} RG")

    def fill_color(self, color: str) -> None:
        r, g, b = hex_to_rgb(color)
        self.ops.append(f"{r:.3f} {g:.3f} {b:.3f} rg")

    def line(self, x1: float, y1: float, x2: float, y2: float, color: str = "#000000", width: float = 0.6, dash: bool = False) -> None:
        self.stroke_color(color)
        self.ops.append(f"{width:.2f} w")
        self.ops.append("[3 2] 0 d" if dash else "[] 0 d")
        self.ops.append(f"{x1:.2f} {y1:.2f} m {x2:.2f} {y2:.2f} l S")

    def rect(self, x: float, y: float, w: float, h: float, fill: str, stroke: str | None = None, width: float = 0.6) -> None:
        self.fill_color(fill)
        if stroke:
            self.stroke_color(stroke)
            self.ops.append(f"{width:.2f} w")
            self.ops.append(f"{x:.2f} {y:.2f} {w:.2f} {h:.2f} re B")
        else:
            self.ops.append(f"{x:.2f} {y:.2f} {w:.2f} {h:.2f} re f")

    def text(self, x: float, y: float, text: object, size: float = 8, color: str = "#000000", align: str = "left") -> None:
        self.fill_color(color)
        escaped = pdf_escape(text)
        width_est = len(str(text)) * size * 0.48
        if align == "center":
            x -= width_est / 2
        elif align == "right":
            x -= width_est
        self.ops.append(f"BT /F1 {size:.1f} Tf {x:.2f} {y:.2f} Td ({escaped}) Tj ET")

    def save(self, path: Path) -> None:
        content = "\n".join(self.ops).encode("latin-1", errors="replace")
        objects = [
            b"<< /Type /Catalog /Pages 2 0 R >>",
            b"<< /Type /Pages /Kids [3 0 R] /Count 1 >>",
            (
                f"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 {self.width:.2f} {self.height:.2f}] "
                f"/Resources << /Font << /F1 4 0 R >> >> /Contents 5 0 R >>"
            ).encode(),
            b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>",
            b"<< /Length " + str(len(content)).encode() + b" >>\nstream\n" + content + b"\nendstream",
        ]
        chunks = [b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n"]
        offsets = [0]
        for idx, obj in enumerate(objects, start=1):
            offsets.append(sum(len(chunk) for chunk in chunks))
            chunks.append(f"{idx} 0 obj\n".encode() + obj + b"\nendobj\n")
        xref_offset = sum(len(chunk) for chunk in chunks)
        chunks.append(f"xref\n0 {len(objects) + 1}\n".encode())
        chunks.append(b"0000000000 65535 f \n")
        for offset in offsets[1:]:
            chunks.append(f"{offset:010d} 00000 n \n".encode())
        chunks.append(
            f"trailer\n<< /Size {len(objects) + 1} /Root 1 0 R >>\nstartxref\n{xref_offset}\n%%EOF\n".encode()
        )
        path.write_bytes(b"".join(chunks))


def ymap(value: float, bottom: float, top: float, ymax: float) -> float:
    return bottom + (value / ymax) * (top - bottom)


def draw_axes(pdf: SimplePDF, left: float, bottom: float, width: float, height: float, ymax: float, ylabel: str | None = None) -> None:
    pdf.line(left, bottom, left, bottom + height, width=0.6)
    pdf.line(left, bottom, left + width, bottom, width=0.6)
    for tick in [0, ymax / 2, ymax]:
        y = ymap(tick, bottom, bottom + height, ymax)
        pdf.line(left - 3, y, left, y, width=0.5)
        pdf.text(left - 5, y - 3, f"{tick:.2f}", size=6.5, align="right")
    if ylabel:
        pdf.text(left - 34, bottom + height + 8, ylabel, size=7)


def draw_bar_panel(
    pdf: SimplePDF,
    left: float,
    bottom: float,
    width: float,
    height: float,
    labels: list[str],
    values: list[float],
    lows: list[float],
    highs: list[float],
    colors: list[str],
    ymax: float,
    title: str,
    panel: str | None = None,
) -> None:
    if panel:
        pdf.text(left - 18, bottom + height + 20, panel, size=10)
    pdf.text(left + width / 2, bottom + height + 14, title, size=8, align="center")
    draw_axes(pdf, left, bottom, width, height, ymax, ylabel="Flattening drop (BA)")
    pdf.line(left, ymap(0, bottom, bottom + height, ymax), left + width, ymap(0, bottom, bottom + height, ymax), dash=True)
    slot = width / len(values)
    bar_w = slot * 0.56
    for i, value in enumerate(values):
        x = left + slot * i + (slot - bar_w) / 2
        y = ymap(value, bottom, bottom + height, ymax)
        pdf.rect(x, bottom, bar_w, y - bottom, fill=colors[i])
        xc = x + bar_w / 2
        lo = ymap(lows[i], bottom, bottom + height, ymax)
        hi = ymap(highs[i], bottom, bottom + height, ymax)
        pdf.line(xc, lo, xc, hi, width=0.7)
        pdf.line(xc - 3, lo, xc + 3, lo, width=0.7)
        pdf.line(xc - 3, hi, xc + 3, hi, width=0.7)
        for j, part in enumerate(labels[i].split("\n")):
            pdf.text(xc, bottom - 12 - j * 8, part, size=6.5, align="center")


